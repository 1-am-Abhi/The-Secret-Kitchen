/* ==========================================================================
   Derived selectors
   Pure functions over the catalogue — reused by pages, the API layer and the
   admin panel so query logic is never duplicated in a component.
   ========================================================================== */

export const categoryBySlug = new Map(categories.map((c) => [c.slug, c]));
export const itemById = new Map(menuItems.map((i) => [i.id, i]));
export const itemBySlug = new Map(menuItems.map((i) => [i.slug, i]));

export function getItemsByCategory(slug: CategorySlug): MenuItem[] {
  return menuItems.filter((item) => item.category === slug);
}

export function getBestSellers(limit = 8): MenuItem[] {
  return menuItems
    .filter((item) => item.tags.includes("bestseller"))
    .sort((a, b) => b.ratingCount - a.ratingCount)
    .slice(0, limit);
}

export function getChefSpecials(limit = 6): MenuItem[] {
  return menuItems.filter((item) => item.tags.includes("chef-special")).slice(0, limit);
}

/**
 * "Today's Special" rotates daily without a database. The day-of-year seeds a
 * fixed offset into the specials pool, so every visitor sees the same set on a
 * given date and the selection changes at midnight.
 */
export function getTodaysSpecial(limit = 4, today = new Date()): MenuItem[] {
  const pool = menuItems.filter(
    (item) => item.tags.includes("chef-special") || item.tags.includes("bestseller"),
  );
  const start = new Date(today.getFullYear(), 0, 0);
  const dayOfYear = Math.floor((today.getTime() - start.getTime()) / 86_400_000);
  const offset = dayOfYear % pool.length;
  // Rotate the pool by `offset` and take the first `limit` entries.
  return [...pool.slice(offset), ...pool.slice(0, offset)].slice(0, limit);
}

export function getPopularCategories(limit = 6): MenuCategory[] {
  const countByCategory = menuItems.reduce<Record<string, number>>((acc, item) => {
    acc[item.category] = (acc[item.category] ?? 0) + item.ratingCount;
    return acc;
  }, {});
  return [...categories]
    .sort((a, b) => (countByCategory[b.slug] ?? 0) - (countByCategory[a.slug] ?? 0))
    .slice(0, limit);
}

export function getRelatedItems(item: MenuItem, limit = 4): MenuItem[] {
  return menuItems
    .filter((other) => other.category === item.category && other.id !== item.id)
    .slice(0, limit);
}

/** Cheapest item in a category — powers the "from ₹49" label on category cards. */
export function getCategoryStartingPrice(slug: CategorySlug): number {
  const prices = getItemsByCategory(slug).map((item) => item.price);
  return prices.length ? Math.min(...prices) : 0;
}

export function countItemsInCategory(slug: CategorySlug): number {
  return getItemsByCategory(slug).length;
}

export const priceBounds = {
  min: Math.min(...menuItems.map((i) => i.price)),
  max: Math.max(...menuItems.map((i) => i.price)),
};
